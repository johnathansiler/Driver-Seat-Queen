// Get image path for a question ID
export const getQuestionImage = (questionId) => {
  const filename = questionImages[questionId]
  if (!filename) return null
  return `${import.meta.env.BASE_URL}images/${filename}`
}

// Check if question has an image
export const hasImage = (questionId) => {
  return questionImages.hasOwnProperty(questionId)
}
